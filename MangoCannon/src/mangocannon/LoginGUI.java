    public boolean UserAuth(String theUsername, String thePassword){
        for (int i = 0; i < localUser.size(); i++){
            if(localUser.elementAt(i).getUsername().contentEquals(theUsername) && localUser.elementAt(i).getPassword().contentEquals(thePassword)){
                //Set userFound to true
                return true;
            } 
        }
        return false;
    }
    
